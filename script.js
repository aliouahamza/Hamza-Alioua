let currentLang = 'ar';
let currentTheme = 'light';

// Initialize the website
document.addEventListener('DOMContentLoaded', function() {
    // Check for saved preferences
    const savedLang = localStorage.getItem('preferred-language') || 'ar';
    const savedTheme = localStorage.getItem('preferred-theme') || 'light';
    
    changeLanguage(savedLang);
    if (savedTheme === 'dark') {
        toggleTheme();
    }
});

// Language change function
function changeLanguage(lang) {
    currentLang = lang;
    
    // Update document attributes
    document.documentElement.lang = lang;
    document.body.dir = lang === 'ar' ? 'rtl' : 'ltr';
    
    // Update all translatable elements
    document.querySelectorAll('[data-key]').forEach(element => {
        const key = element.getAttribute('data-key');
        if (translations[lang] && translations[lang][key]) {
            if (key === 'footer') {
                element.innerHTML = translations[lang][key];
            } else {
                element.textContent = translations[lang][key];
            }
        }
    });
    
    // Update language selector
    const languageSelector = document.querySelector('.language-selector');
    languageSelector.value = lang;
    
    // Save preference
    localStorage.setItem('preferred-language', lang);
}

// Theme toggle function
function toggleTheme() {
    const body = document.body;
    const themeToggle = document.querySelector('.theme-toggle i');
    
    if (currentTheme === 'light') {
        body.setAttribute('data-theme', 'dark');
        themeToggle.className = 'fas fa-sun';
        currentTheme = 'dark';
    } else {
        body.removeAttribute('data-theme');
        themeToggle.className = 'fas fa-moon';
        currentTheme = 'light';
    }
    
    // Save preference
    localStorage.setItem('preferred-theme', currentTheme);
}

// Master details toggle
function toggleMasterDetails() {
    const details = document.getElementById('masterDetails');
    const btn = document.querySelector('.master-details-btn');
    
    if (details.classList.contains('show')) {
        details.classList.remove('show');
        btn.setAttribute('data-key', 'show-details');
        btn.textContent = translations[currentLang]['show-details'];
    } else {
        details.classList.add('show');
        btn.setAttribute('data-key', 'hide-details');
        btn.textContent = translations[currentLang]['hide-details'];
    }
}

